package com.acculytixs.wayuparty.dto.user;

public class UserGuestClubsDetailsDTO {

	private String club;
	
	private String clubLocation;
	
	private String clubImage;
	
	private String guestDate;
	
	private String guestCode;
	
	private String description;
	
	private String qrCode;
	
	

	public String getClub() {
		return club;
	}

	public void setClub(String club) {
		this.club = club;
	}

	public String getClubLocation() {
		return clubLocation;
	}

	public void setClubLocation(String clubLocation) {
		this.clubLocation = clubLocation;
	}

	public String getClubImage() {
		return clubImage;
	}

	public void setClubImage(String clubImage) {
		this.clubImage = clubImage;
	}

	public String getGuestDate() {
		return guestDate;
	}

	public void setGuestDate(String guestDate) {
		this.guestDate = guestDate;
	}

	public String getGuestCode() {
		return guestCode;
	}

	public void setGuestCode(String guestCode) {
		this.guestCode = guestCode;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getQrCode() {
		return qrCode;
	}

	public void setQrCode(String qrCode) {
		this.qrCode = qrCode;
	}
	
	
	
	
}
